    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/PharmaDb", "User1","User1");
            PreparedStatement add = Con.prepareStatement("insert into AGENTS values(?,?,?,?,?,?,?)");
            add.setInt(1, Integer.valueOf(a_id.getText()));
            add.setString(2, a_name.getText());
            add.setInt(3, Integer.valueOf(a_age.getText()));
            add.setString(4, a_password.getText());
            add.setString(5, a_phone.getText());
            add.setString(6, a_gender.getSelectedItem().toString());
            add.setString(7, a_email.getText());
            
            int row = add.executeUpdate();
            SelectMed();
            Con.close(); 
            JOptionPane.showMessageDialog(this, "Agent Added Successfully");
        
        }catch(org.apache.derby.shared.common.error.DerbySQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(this, "Error: Constraint violation!");
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(this, "Error: A SQL exception occured");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddMouseClicked

