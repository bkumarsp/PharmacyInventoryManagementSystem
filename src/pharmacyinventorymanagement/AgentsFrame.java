    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked

        if (a_id.getText().isEmpty() || a_name.getText().isEmpty() || a_age.getText().isEmpty() || a_phone.getText().isEmpty() || a_password.getText().isEmpty() || a_email.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information! please fillout all the fields");
        }
        else
        {
            try{
               
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/PharmaDb", "User1","User1");
               
                int Id = Integer.valueOf(a_id.getText());
                String retriveId = "Select A_ID from User1.AGENTS where A_ID="+Id; 
                Statement select = Con.createStatement();
                ResultSet row = select.executeQuery(retriveId);

                if(!row.next()){
                    JOptionPane.showMessageDialog(this, "Agent "+Id+" Unavailable! Please enter valid ID to update");
                }else{
                
                
                    String UpdateQuery = "Update User1.AGENTS set A_NAME = '"+a_name.getText()+"'"+",A_AGE = "+a_age.getText()+",A_PHONE = '"+a_phone.getText()+"',A_PASSWORD = '"+a_password.getText()+"',A_GENDER = '"+a_gender.getSelectedItem().toString()+"'"+" where A_ID = "+a_id.getText();
                    Statement Add = Con.createStatement();
                    Add.executeUpdate(UpdateQuery);
                    JOptionPane.showMessageDialog(this, "Agent Updated Successfully");

                    SelectMed();
                }
            }catch(org.apache.derby.shared.common.error.DerbySQLIntegrityConstraintViolationException e){
                JOptionPane.showMessageDialog(this, "Error: check your internet");
            }catch(SQLException e)
            {
                JOptionPane.showMessageDialog(this, "Error: A SQL exception occured");
                e.printStackTrace();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Error: please fill all details correctly!");

            }
            


        }

    }//GEN-LAST:event_btnUpdateMouseClicked

